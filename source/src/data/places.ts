/* Blandede steder til inspiration (pick N tilfældigt) */
export function getRandomPlaces(n: number): Place[] {
  const shuffled = [...ALL_PLACES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}

/* Steder til "Verden derude" inspiration — spredt over hele DK */
export function getInspirationPlaces(): Place[] {
  // Pick one from each category for variety
  const categories: PlaceCategory[] = [
    "strand", "shelter", "mtb", "vandring", "havnebad",
    "naturlegeplads", "klatring", "hundeskov", "naturpark",
  ];
  const picks: Place[] = [];
  for (const cat of categories) {
    const items = ALL_PLACES.filter(p => p.category === cat);
    if (items.length) {
      picks.push(items[Math.floor(Math.random() * items.length)]);
    }
  }
  return picks;
}
