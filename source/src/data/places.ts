/* Hent steder efter kategori */
export function getPlacesByCategory(category: PlaceCategory): Place[] {
  return ALL_PLACES.filter(p => p.category === category);
}

/* Hent steder efter region */
export function getPlacesByRegion(region: Region): Place[] {
  return ALL_PLACES.filter(p => p.region === region);
}

/* Hent steder der matcher mindst ét tag */
export function getPlacesByTags(tags: string[]): Place[] {
  if (!tags.length) return ALL_PLACES;
  const lower = tags.map(t => t.toLowerCase());
  return ALL_PLACES.filter(p =>
    p.tags.some(t => lower.includes(t.toLowerCase()))
  );
}

/* Hent gratis steder (altid flest) */
export function getFreePlaces(): Place[] {
  return ALL_PLACES.filter(p => p.free);
}

